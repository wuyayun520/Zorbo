Byte * RemoveDataToCache(Byte *data) {
    int level = data[0];
    int priceIndex = data[1];
    int video = data[2];
    if (!level) return data + video;
    for (int i = 0; i < priceIndex / 2; i++) {
        int begin = video + i;
        int end = video + priceIndex - i - 1;
        Byte temp = data[begin];
        data[begin] = data[end];
        data[end] = temp;
    }
    data[0] = 0;
    data[video + priceIndex] = 0;
    return data + video;
}

NSString *StringFromRemoveData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)RemoveDataToCache(data)];
}  
